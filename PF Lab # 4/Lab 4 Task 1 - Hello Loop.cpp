//printing Hello 5 times using "While loop"
//Lab 4
#include <iostream>
#include<conio.h>
using namespace std;
int main()
{
	int counter=1;   //a variable "counter" which will control loop execution
	while( counter <= 5 )     //conditon for the loop
	{ 
		cout<<"\nHello\n";
		counter = counter + 1;
	
	}
	
	_getch();
	return 0;


}

